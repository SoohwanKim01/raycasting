#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*cpy;
	int		i;

	i = 0;
	if (!(cpy = (char *)malloc(ft_strlen(str) + 1)))
		return (NULL);
	while (str[i])
	{
		cpy[i] = str[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
